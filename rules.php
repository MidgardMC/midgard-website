            <p>We're pretty laid back, and don't have many rules. But we do require that you follow these simple, common-sense rules that keep the gameplay fair and relaxed for everyone involved. <strong>Please note that our server is 16+ only!</strong></p>

            <p><a class="btn btn-success" href="https://docs.google.com/document/d/1EFTXAGFpxewtfJz2Z84pFjjzoqI38OAehEI4YNLtsIM/edit?usp=sharing" target="_blank">Rules Doc</a></p>


            <h2>Our Rules</h2>
            <hr />

            <ul>
                <li><strong>No Griefing</strong> &mdash; You are responsible for all activity that originates from <em>your</em> Minecraft account. Make sure your password is secure and do not give it out to anyone. Note that altering public roads and builds without staff permission is considered griefing. TNT is prohibited and fire spread is disabled. Griefing is considered but not limited to the following: Destroying builds/land, 1x1 holes to bedrock, TNT/Lava placement with the intention to destroy, etc.</li>
                <li><strong>No Stealing</strong> &mdash; If you don't have explicit permission to touch or take something, don't mess with it. Do not use private farms or grinders without permission from the owner.</li>
                <li><strong>No Hacked Clients</strong> &mdash; Hacked Clients, X-ray, Flight, Speed, Insta-break, block duplication, gamma cheats and other exploits are prohibited.</li>
                <li><strong>Crowding</strong> &mdash; Keep your builds at a reasonable distance between other players unless you have permission. This is a big map, so there is plenty of room!</li>
                <li><strong>Lag Machines</strong> &mdash; Please keep your hopper/sorter count and size in check. Larger Redstone builds should have a switch to be turned off when you're not using them. Please keep mob farms to 100 entities.</li>
                <li><strong>Land Claims</strong> &mdash; Do not claim land if you don't plan to build on it. Abandoned builds will be removed at the staff's discretion.</li>
                <li><strong>Mining</strong> &mdash; Use the Resource world for mining and cutting trees. Underground mining is permitted on the main world only within your property's borders. Do not leave floating trees.</li>
                <li><strong>Building</strong> &mdash; Disruptive, offensive, abandoned or otherwise incomplete builds may be relocated by staff without warning.</li>
            </ul>

            <h2>Code of Conduct</h2>
            <hr />

            <ul>
                <li><strong>Server Chat</strong> &mdash; Please refrain from spamming chat and using all caps. All server chat (voice and text) are in English.</li>
                <li><strong>Voice Chat</strong> &mdash; Refrain from playing loud music or sound files. Minimal use of sound boards is OK as long as it is not disruptive. If you are streaming voice chat, ensure that you have the consent of all players on the Mumble or Discord server. Short AFK periods are fine, but excessive AFK periods may get you moved to the AFK channels. Please make sure your audio levels are set up properly. Be respectful and don't be rude or vulgar.</li>
                <li><strong>Maturity</strong> &mdash; We cater to mature, seasoned players, so that means refraining from excessive swearing, hate speech, religious and political discussions.</li>
                <li><strong>AFK Timer</strong> &mdash; Please do not circumvent the AFK timer, especially for player material gain, this counts as cheating.</li>
                <li><strong>Cyberbullying</strong> &mdash; We pride ourselves on providing a safe, fun environment to play. Avoid mocking and bullying staff and other players.</li>
                <li><strong>User Poaching</strong> &mdash; Please refrain from advertising other Minecraft servers.</li>
                <li><strong>In-Game Currency</strong> &mdash; Our server uses in-game currency, please refrain from advertising the sale of goods and services outside of the game.</li>
            </ul>

            <h2>Building Guidelines</h2>
            <hr />

            <ul>
                <li><strong>Roads</strong> &mdash; Please do not build up against roads. Ensure that your walkways are centered between the lamps on the road. There should be at least 12-15 blocks between your build and the road. Do not alter roads without staff approval.</li> 
                <li><strong>Bridges</strong> &mdash; Be sure to make bridges at least two blocks high so boats can pass under.</li>
                <li><strong>Towns</strong> &mdash; There are towns and developments managed by other players, please get permission from the responsible parties before building in them. You are also free to start your own development if land is available.</li> 
            </ul>

            <h2>Helpful Tips</h2>
            <hr />

            <ul>
                <li><strong>Streamers Welcome</strong> &mdash; We love the positive feedback streaming can bring. We only ask that you keep the address off of the stream to prevent attacks.</li>
                <li><strong>Resources</strong> &mdash; In addition to our main survival world, we also have worlds dedicated to gathering resources. Your inventory does transfer over with the exception of "Flatland", which is our creative test server. The resource worlds are reset every 90-120 days as needed.</li>
                <li><strong>Protection</strong> &mdash; Our server certain protections to keep your stuff secure. Doors, chests, furnaces, brewing stands, and other containers can be locked using the "/lock" command. We also have region protection, which is used to secure builds.</li>
                <li><strong>Public Farms</strong> &mdash; We have public food and XP farms available for your use. Use the warps to take advantage of them. <strong>Please replant or breed anything you harvest!</strong></li>
                <li><strong>Approved Clients/Mods</strong> &mdash; We currently allow pre-approved client-side mods such as Optifine, InvTweaks, Schematica, any minimap, Shaders, Zyin's HUD, and WorldEditCUI. Remember that hacked clients are strictly prohibited.</li>
                <li><strong>Public Roads</strong> &mdash; Midgard has an extensive and impressive public road system with a uniform design. Each Bridge and Tunnel has an ID number for easy identification in the event of griefing or damage. Please do not alter these roads, if you need any accomodations or modifications, please contact staff.</li>
                <li><strong>Voice Chat</strong> &mdash; We have Discord and Mumble servers available for use. Same rules apply as above.</li>
                <li><strong>Donations</strong> &mdash; Midgard offers all of its services free of charge. We do not solicit payments and do not have paid user enhanced levels. If you wish to donate, please ask any staff member. Any and all donations will be put towards hardware maintenance and upgrade/operation costs. Donations are appreciated but absolutely not required.</li>
                
            </ul>

            <h2>Access Policy</h2>
            <hr />

            <p>Midgard is a whitelist only server. Your access must be pre-approved and granted by staff. We pride ourselves on our history of offering a safe, fun place for our community to play. If you must be away for an extended period, just let us know. We know that real life takes priority over any game. Your build and possessions will remain safe and untouched until you can return. We put great effort into ensuring that the server is reliable and available any time you wish to play or use the Mumble server.</p>

            <p>We operate 24/7/365 and we are proud of our high uptime and reliability. Please follow us on Twitter (@midgardmc) and check there for any downtime announcements as sometimes these are necessary to keep things up to date and running smoothly. Above all else, enjoy your stay!</p>

            <h2>Ban Policy</h2>
            <hr />

            <p>We pride ourselves in having a history where very few players have been banned. For a very minor infraction, you may be removed from the whitelist for a determined period as decided by staff. Major infractions will result in permanent removal from our community. Very major infractions (cheating and griefing) will result in a hard ban and other communities may be alerted. Any DDoS attacks on the server will be investigated and the authorities may be contacted.</p>

            <h2>Player Agreement</h2>
            <hr />

            <p>By gaining access to the server, you agree to follow all rules and guidelines listed above and in the linked document. Access to this server is a privilege and not a right.</p>


