            <p><em>Orignally written by jesskitten, revised in 2018 by DisturbedCrime</em></p>
            
            <p>This is a quick screenshot-based primer on how to to use the SignShop plugin for buying, selling, and donating items.</p>

            <h2>Common Bugs</h2>
            <ul>
                <li>If you are setting up a [Buy] or [Sell] sign, <strong>do not include more than period in the last line.</strong> The item will be flagged as being on sale for 0 coins.</li>
                <li>You cannot use signs while holding dye items.</li>
                <li>As of the last patch, the first line of the sign gets indented.</li>
                <li>As of the last patch, you sometimes may not be able to break a sign.</li>
            </ul>

            <br />

            <h2>Selling Items - Buy Signs</h2>
            <hr />
            
            <p>[Buy] Signs allow a player to buy things from you &mdash; if you are the owner of a shop and want to sell things to customers, this is the sign you want to use.</p>

            <p><strong>To create a [Buy] sign, place a sign with the following:</strong></p>

            <ul>
                <li><strong>First Line:</strong> [Buy]</li>
                <li><strong>Second/Third Line:</strong> Item/Item description</li>
                <li><strong>Fourth Line:</strong> Price in any format ($0, $0.00, or 0)</li>
            </ul>

            <br />

            <img src="img/ss/ss1.png" class="img-responsive" />

            <p class="bg-info text-center">It doesn't matter what format you put your price in!</p>

            <br />

            <h3>Connecting Your Chests</h3>

            <p>You must then select a target chest, and fill it with exactly the type and number of items you wish to sell <em>per transaction</em>. <strong> Do not place your full stock in the chest yet</strong>.</p>

            <br />

            <img src="img/ss/ss2.png" class="img-responsive" />

            <p class="bg-info text-center">In the image above, the chest is set up to sell (1) slime ball for 5 coins. Ensure that when connecting your chests, that exactly (1) slime ball is in the chest.</p>

            <br />

            <img src="img/ss/ss3.png" class="img-responsive" />

            <p class="bg-info text-center">Once the item that I want to sell is placed in the chest, left OR right click the sign with Redstone dust. This marks the chest's location. Note the status messages after each step!</p>

            <br />

            <img src="img/ss/ss4.png" class="img-responsive" />

            <p class="bg-info text-center">Lastly, left click on the chest containing your item again with the Redstone dust.</p>

            <p>Again, take note of the status messages after each step. If you are selling multiple items from one sign (a kit), the last status message should summarize all of the items per transaction. Make sure there are no mistakes! <strong>And remember to lock your chests!</strong> </p>

