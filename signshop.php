            <h2>Generic Donations - DonateHand Signs</h2>
            <hr />
            
            <p>[DonateHand] Signs allow a player to donate whatever they are "holding" to you &mdash; the user must put the item they want to donate in their hotbar, select it, and then right click on your sign. They are easier to set up than other signs, and are not tied to just any one item. They are often used as generic donation chests in shops, or in a mining or farming outpost to donate resources.</p>

            <p><strong>To create a [DonateHand] sign, place a sign with the following:</strong>

            <ul>
                <li><strong>First Line:</strong> [DonateHand]</li>
                <li><strong>Second/Third/Fourth Line:</strong> Any Text</li>
            </ul>
            
            </p>

            <img src="img/ss/ss13.png" class="img-responsive" />

            <p class="bg-info text-center">It doesn't matter what text you put on the DonateHand sign!</p>

            <br />

            <h3>Connecting Your Chests</h3>

            <p>You will "hit" the sign with Redstone dust, select your target chest, and "hit" that too. Make sure the chest is unlocked and that you can use it, or you will not be able to set it as a DonateHand chest.

