            <h2>Donating - Donate Signs</h2>
            <hr />
            
            <p>[Donate] Signs allow a player to donate things to you &mdash; they are set up exactly the same way as Buy/Sell signs, but are seldom used because they are tied specifically to one item.</p>

            <p><strong>To create a [Donate] sign, place a sign with the following:</strong>

            <ul>
                <li><strong>First Line:</strong> [Donate]</li>
                <li><strong>Second/Third/Fourth Line:</strong> Any Text</li>
            </ul>
            
            </p>

